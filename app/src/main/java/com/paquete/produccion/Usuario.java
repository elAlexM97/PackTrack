package com.paquete.produccion;

public class Usuario {
    private String id;
    private String email;
    private String usuario;
    private String contraseña;
    private String userType; // "produccion" o "materiales"
    private String nombre;
    private String area;
    private String fcmToken; // Token para notificaciones push

    public Usuario() {
    }

    public Usuario(String email, String usuario, String contraseña, String userType) {
        this.email = email;
        this.usuario = usuario;
        this.contraseña = contraseña;
        this.userType = userType;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getFcmToken() {
        return fcmToken;
    }

    public void setFcmToken(String fcmToken) {
        this.fcmToken = fcmToken;
    }
}
